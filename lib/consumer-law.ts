/**
 * 민원 제목+내용 기반으로 관련 법령 항목을 반환합니다 (최대 2개).
 */
export function findRelevantLaws(title: string, content: string): LawItem[] {
  const query = `${title} ${content}`.toLowerCase()
  const words = query.split(/\s+/).filter((w) => w.length > 1)

  return CONSUMER_LAW_ITEMS
    .map((item) => {
      const score = item.keywords.filter((k) => query.includes(k)).length
        + words.filter((w) => item.keywords.some((k) => k.includes(w))).length * 0.5
      return { item, score }
    })
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 2)
    .map(({ item }) => item)
}
